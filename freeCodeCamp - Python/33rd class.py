# XML
# extensible markup language
# primary purpose is to help information system share structured data
# it started as a simplified subset of the Standard Generalized Markup Language
# (SGML), and is designed to be relatively human-legible

# XML Terminology
# tags  indicate the begginning and ending of elements
# attributes - keyword/value pairs on the opening tag os XML
# serialize/ de-serialize - convert data in one program into a common
# format that can be stored and/or transmitted between systems in a programming
# language-independent manner

# XML Basics:
# start tag, end tag, text content, attribute, self closing tag

# <person>
#  <name> Chuck </name
#  <phone type="intl">
#  + 1 734 303 4456
#  </phone>
#  <email hide="yes">
# </person>

